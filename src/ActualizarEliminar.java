    private void iniciarCampos(Empleado empleado){
        
        this.jLabel9.setText("Empleado: "+empleado.getId());
        this.jTextField1.setText(empleado.getNombre());
        this.jTextField2.setText(""+empleado.getApellido());
        this.jTextField3.setText(""+empleado.getCorreo());
        this.jTextField4.setText(""+empleado.getCelular());
        this.jTextField5.setText(""+empleado.getSalario());
        this.jTextField6.setText(""+empleado.getCargo());
        
        if (empleado.isEsProveedor()) {
            this.jCheckBox1.setSelected(true);
        } else {
            this.jCheckBox1.setSelected(false);
        }
        
    }

